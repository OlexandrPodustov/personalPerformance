// Verse returns a chunk from song
func Verse(in int) string {
	var cnt, start int
	var result string
	for i := range e {
		if string(e[i]) == "\n" {
			if string(e[i+1]) == "\n" {
				cnt++
				if in == cnt {
					result = e[start:i]
				} else if in == cnt+1 {
					result = e[i+2:]
				}
				start = i + 2
			}
		}
	}

	return result
}

